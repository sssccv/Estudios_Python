# Variables en Python

# Declaracion e iniciacion de variables
edad = 28
altura = 1.65
pais = "México"

# Acceder a las variables (se pone el nombre de la variable, pero ya tiene que estar definida anteriormente)
print("Valores iniciales: ")
print("edad:", edad)
print("altura:", altura)
print("pais:", pais)

# Modificar el valor de una variable
edad = 30
altura = 1.68

print("Valores modificados:")
print("edad:", edad)
print("altura:", altura)
print("pais:", pais)

 #  En python el tipo es dinamico (no se queda a fuerza la declaracion inicial de la variable)
edad="treinta"
print("edad:",edad)

#Si queremos acceder a una variable no declarada manda error
