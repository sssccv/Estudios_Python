# Reglas estrictas:
# Los nombres de variables pueden tener letras (mayúsculas o minúsculas), dígitos y guiones bajos (_)
# El nombre NO puede comenzar con dígitos
# NO se pueden usar palabras reservadas del lenguaje (keywords) (for, if, class, try, etc)
# Python es sensible a mayúsculas y minúsculas, por ejemplo, la variable "mi_nombre" no sería la misma que "Mi_nombre"

# Buenas prácticas:
# Snake case: al nombrar tus variables es recomendable usar snake case, es decir, palabras en minúsculas separadas por guiones bajos, por ejemplo: nombre_de_usuario
# Nombres descriprivos: Los nombres de las variables deben describir el contenido de la variable, para que se usan, por ejemplo, el lugar de solo poner "e = 10" pon "Edad = 10"
# Evitar nombres de un solo caracter ya que no son descriptivos y llegan a ser confusos
