# Una cadena (string) es un dato que almacena caracteres
# Se deben encerrar entre "" o ''
# Pueden ser letras, números, símbolos o espacios

Cadena1 = "Hola"
print(Cadena1)
print()
Cadena1 = 'Adios'
print(Cadena1)
print()

# Para hacer una cadena con saltos de linea se escribe entre triples comillas ''' texto '''

Cadena3 = '''Este es un ejemplo
    de multiples lineas
        en una cadena'''
print(Cadena3)