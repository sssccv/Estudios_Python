# Los caracteres de una cadena están indexados de manera secuencial
# Por lo tanto, podemos acceder a cada caracter indicando el índice del caracter que queremos recuperar

Cadena1 = "Hola mundo"

# Cadena1 = 0"H" 1"o" 2"l" 3"a" 4" " 5"m" 6"u" 7"n" 8"d" 9"o"
print(Cadena1)

# Recuperar el primer caracter de la cadena

primer_caracter = Cadena1[0] # Recupera el caracter de H

print(primer_caracter)

ultimo_caracter = Cadena1[9]
print(ultimo_caracter)