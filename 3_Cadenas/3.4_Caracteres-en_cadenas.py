# Las cadenas pueden incluir caracteres especiales
# Estos caracteres se introducen usando la diagonal invertida \

# Nueva linea : "\n" inserta un salto de linea
ej1= "esto es \nun ejemplo de salto de línea"
print(ej1)
print()

# Tabulación : "\t" inserta una tabulación, es util para alinear textos
ej2 = "esto es \t un ejemplo de tabulación"
print(ej2)
print()

# Comilla simple "\'" permite incluir comillas simples adentro de comillas
ej3 = "\'esto \' es un ejemplo de comillas simples"
print(ej3)
print()

# Comilla doble "\"" permite incluir comillas adentro de comillas
ej4 = "\"esto \" es un ejemplo de comillas dobles"
print(ej4)
print()

# Barra invertida "\\" permite meter una barra invertida
ej5 = "esto es un ejemplo de \\ barras invertidas"
print(ej5)
print()

# Ejemplos
print("-----Ejemplos-----")
print("Hola \nMundo")
print("Texto1 \t Texto2")
print("\'Comillas simples\'")
print("\"Comillas dobles\" ")
print("1\\10")

