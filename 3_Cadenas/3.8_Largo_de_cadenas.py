# Para obtener la longitud de una cadena, se utiliza la funcion de len()
# len() funciona con varios tipos de datos, incluyendo cadenas, listas, etc
# Al calcular el largo de una cadena  cuenta todos los caracteres de la misma, incluyendo espacios, caracteres especiales, etc

cadena= "Hola me llamo juan!" # Aqui declaramos la cadena
longitud =len(cadena) # Aqui conseguimos el largo de la cadena
print(longitud) # Aqui se imprime SOLO el largo de la cadena
print(f"la cadena de \'{cadena}\' tiene una longitud de {longitud} caracteres")# Aqui mandamos a imprimir un mensaje con la cadena y su largo