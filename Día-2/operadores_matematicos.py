x=6
y=2
z=7
print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")

#Division al piso cuando divides un número y lleva el resultado hacia el entero (si sale 3.5 se pone solo el 3), se pone con // en lugar de una sola /

print(f"{z} dividido al piso de {y} es igual a {z//y}")

#Modulo es el restante de una operación ( si divides 7 entre 2 el 2 solo cabe 3 veces en el 7 y sobra uno, el uno es el módulo), se pone con %

print(f"{z} modulo de {y} es igual a {z%y}")

#potencia se pone con ** (hace que el primer número se multiplique (segundo número) de veces por si mismo

print(f"{x} elevado a la {y} = {x**y}")

#raíz cuadrada se escribe como **0.5

print(f"La raíz cuadrada de {x} es {x**0.5}")